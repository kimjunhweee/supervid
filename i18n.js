// === Core i18n functions ===
let _currentLang = localStorage.getItem('creatorhub_lang') || 'ko';

function getLang() {
    return _currentLang;
}

function setLang(lang) {
    if (lang !== 'ko' && lang !== 'en') return;
    _currentLang = lang;
    localStorage.setItem('creatorhub_lang', lang);
}

function t(key, params) {
    let str = I18N[_currentLang]?.[key] ?? I18N['ko']?.[key] ?? key;
    if (params) {
        Object.keys(params).forEach(k => {
            str = str.replace(new RegExp('\\{\\{' + k + '\\}\\}', 'g'), params[k]);
        });
    }
    return str;
}

function applyI18nToDOM() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (key) el.textContent = t(key);
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (key) el.placeholder = t(key);
    });
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
        const key = el.getAttribute('data-i18n-title');
        if (key) el.title = t(key);
    });
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
        const key = el.getAttribute('data-i18n-html');
        if (key) el.innerHTML = t(key);
    });
}

function getWelcomeMessages() {
    return WELCOME_MESSAGES_I18N[_currentLang] || WELCOME_MESSAGES_I18N['ko'];
}
